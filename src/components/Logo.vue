<script>
export default {
  data() {
    return {
      wasHovering: false,
      isHovering: false,
      shouldAnimate: false,
      shouldReverse: false,
      interval: null,
      svg: null,
    };
  },
  mounted() {
    this.svg = document.getElementById("interactablelogo");
  },
  methods: {
    mouseEnter() {
      this.isHovering = true;

      clearInterval(this.interval);
      this.checkState();
      this.interval = setInterval(this.checkState, 10000);
    },
    mouseLeave() {
      this.isHovering = false;
    },
    checkState() {
      if (this.wasHovering !== this.isHovering) {
        const clone = this.svg.children[0].cloneNode(true);
        this.svg.children[0].remove();
        this.svg.append(clone);

        if (!this.isHovering) {
          clearInterval(this.interval);
        }
      }

      this.shouldAnimate = true;
      this.shouldReverse = !this.isHovering;
      this.wasHovering = this.isHovering;
    },
  },
};
</script>

